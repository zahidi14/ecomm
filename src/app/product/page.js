export async function getStatic() {
  const coll = collection(db, "product");
  const snap = await getDocs(coll);
  const path = snap.docs.map((doc) => ({
    params: { id: doc.id },
  }));

  return {
    path,
    fallback: true,
  };
}
export async function getStaticProps({ params }) {
  const docRef = doc(db, "product", params.id);
  const docSnap = await getDoc(docRef);

  if (!docSnap.exists()) {
    return {
      notFound: true,
    };
  }
  return {
    props: {
      product: docSnap.data(),
    },
    revalidate: 10,
  };
}
